		<BookTextTitle>Ego satânico</BookTextTitle>
		<p>
			Todos os seres humanos são egocêntricos em maior ou menor grau. Todos (as) possuem problemas
			de vaidade e mania de grandeza.
		</p>
		<p>
			Se você não consegue acreditar na existência de uma entidade não física, que levou o
			egocentrismo às últimas consequências tal como Ha-Satã, tente concebê-lo como um dos
			arquétipos de Carl Jung, ou como um conceito similar ao inconsciente coletivo.
		</p>
		<p>
			Gestalt é o conceito de que o "todo é maior que as partes", ou seja, imagine duas pessoas
			conversando, de um modo geral podemos entender que a conversa é o resultado da interação das
			duas mentes e nada mais; para o conceito de gestalt, o resultado seria uma terceira mente pois
			o resultado obtido jamais poderia ter sido possível sem a interação das duas mentes; logo: o
			todo é maior que as partes.
		</p>
		<BookQuote author="Mateus 18:20" url="https://www.bibliaonline.com.br/acf/mt/18/20+">
			<p>Porque, onde estiverem dois ou três reunidos em meu nome, aí estou eu no meio deles.</p>
		</BookQuote>
		<p>
			Seguindo esta linha de raciocínio, considerando também a possibilidade do pampsiquismo e dos
			campos morfogenéticos e ainda, considerando que existe consenso científico de que os
			pensamentos são físicos (isto é, energia e matéria sutil), podemos considerar que em um nível
			invisível porém real, as mentes humanas geram uma espécie de mente coletiva do mundo, esta
			ideia seria uma variação do conceito de arquétipos de Carl Jung ou algo semelhante ao conceito
			de egrégoras.
		</p>
		<p>
			Existe uma história espiritual narrada em diversas culturas que apresenta o problema do
			egocentrismo como o elemento responsável pela queda espiritual de inúmeros seres, incluindo os
			seres humanos. Esta queda seria não somente moral e filosófica mas também física, pois a
			iniquidade densifica os espíritos e promove a queda; o conto diz o seguinte:
		</p>
		<p>
			Antes dos seres humanos nascerem na carne, eles já existiam na eternidade espiritual. Todos
			nasceram no paraíso, diante de Deus. Estando todos diante de Deus, não podiam manisfestar suas
			individualidades (seus egos) pois tudo era Deus e sobre Deus.
		</p>
		<p>
			Todos os assuntos eram sobre Deus. Todas as narrativas eram sobre os feitos de Deus. A Luz de
			Deus, seu amor e Sua incognoscibilidade eram tão cativantes e interessantes que qualquer obra
			de qualquer outra ser não despertava interesse de ninguém.
		</p>
		<p>
			Deus era perpetuamente o centro das atenções, a estrela do show. Algumas versões do conto
			dizem que estes seres que rodeavam a Deus começaram a sentir inveja de toda esta glória.
			Outras versões dizem que gradualmente, um desejo de se manifestar como individualidade foi
			nascendo no coração de alguns.
		</p>
		<p>
			Essa necessidade foi crescendo gradualmente ao ponto de que estes seres começaram a cobiçar o
			lugar de Deus, não se contentaram apenas com o desejo de se manifestar como individualidade,
			estas almas desejaram ser o centro das atenções no lugar de Deus.
		</p>
		<p>
			De certa forma, este é o mito da queda dos anjos contada de outra maneira, o contexto moral é
			o mesmo. O desejo de se manifestar como indivíduo se transforma em vaidade e a alma passa a
			desejar se sobrepor à força mais poderosa do universo.
		</p>
		<p>
			Algumas versões do conto dizem que estas almas invejaram a Deus e por isso caíram na
			materialidade para aprenderem a se contentar com o fato de que não são e nunca poderão ser "O
			Deus".
		</p>
		<p>
			Em outras versões, é dito que estas almas se reuniram e com seu poder mental combinado,
			criaram esta realidade material, onde Deus está "escondido" e estando Ele "longe" da vista das
			pessoas, tais almas podem ocupar o lugar de "estrela do show".
		</p>
		<p>
			Esta é a mente satânica; querer ser Deus. Podemos resumir a vaidade como se achar ser mais do
			que é ou desejar ser algo superior àquilo que se pode ser, ainda que tal superioridade seja
			subjetiva.
		</p>
		<BookFigure src="/image/Michelangelo_Caravaggio_065.jpg" caption="Narciso" alt="Narciso" />
		<p>Esta manifestação satânica se dá tanto no macrocosmo quanto no microcosmo.</p>
		<p>No macro temos Ha-Satan, ou a mente coletiva.</p>
		<p>
			No microcosmo, isto se manifesta no coração do homem como uma insubordinação à vontade de
			Deus.
		</p>
		<p>
			Por mais incrível que possa parecer, o ateísmo em muitos casos é uma questão de escolha
			(consciente ou não). Por causa de nossa mente silogística, antes de se aceitar uma ideia, a
			nossa mente calcula muito rapidamente todas as conexões e possíveis consequências de se
			aceitar aquela ideia. Nem sempre o processo de assimilação se dá de forma exclusivamente
			mental, às vezes as escolhas são emocionais (embora possam parecer conclusões totalmente
			lógicas e desprovidas de emoção).
		</p>
		<p>
			Muitas das versões de "Deus" apresentadas neste planeta não são muito atraentes. Este
			conceitos fazem parte do consciente coletivo da humanidade e praticamente todos possuem sua
			versão de Deus.
		</p>
		<p>
			Um exemplo simples que pode causar rejeição imediata, uma pessoa totalmente estereotipada com
			trejeitos de sua religião apresenta seu Deus. A pessoa possui aquele aspecto padronizado em
			seu modo de vestir, falar, se relacionar etc. Ela considera tudo como pecado e se acha
			superior aos outros por causa de sua crença.
		</p>
		<p>
			Assim que o Deus é apresentado, o silogismo mental faz com que o ouvinte se imagine dentro
			daquele estereotipo e a rejeição pode ocorrer em função de questões secundárias.
		</p>
		<p>
			Outro fator muito comum para a rejeição é o ego satânico. Certas pessoas não admitem de forma
			alguma que exista alguém que possa "brilhar" mais que ela ou simplesmente não conseguem
			aceitar a ideia de que alguém possa ter poder o suficiente para impedi-las de realizarem as
			suas vontades.
		</p>
		<p>
			É importante compreender este mecanismo interno pois em diversas circunstâncias, acreditar é
			uma escolha, as vezes a realidade se impõe e nossa sensorialidade não nos permite negá-la, mas
			não é sempre assim. Sabe-se que a memória humana é seletiva, trazendo ao consciente somente
			aquilo que é considerado importante e sabemos também da persuasão que acontece por meio da
			repetição.
		</p>
		<p>
			Nosso conceito de lógica pode ser drasticamente alterado dependendo do tipo de informação que
			estamos expostos ao longo do tempo. Vemos nesta chamada "sociedade liquida", pessoas negando
			realidades óbvias que estão diante dos próprios olhos pelo simples fato de terem sido
			programadas para acreditarem que a realidade subjetiva interna é mais importante e mais real
			que a realidade objetiva externa. Ambas as visões são importantes obviamente; a realidade é
			simultaneamente percebida e interpretada; mas isso tem um limite, quando a subjetividade
			começa a dizer que um quadrado é na verdade um círculo (só porque o quadrado é inconveniente
			no momento) ou que A é igual a Z, não estamos falando de empoderamento, mas sim de histeria e
			alienação.
		</p>
		<p>
			Dito isso, para que você consiga colocar em prática o caminho que estou sugerindo, é preciso
			uma firme vontade de encontrar a verdade que te libertará de grilhões que talvez você
			desconheça a existência.
		</p>
