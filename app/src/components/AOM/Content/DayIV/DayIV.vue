			dicotômico pode ser muito saudável, possibilitando a síntese dos antagonismos e por fim uma
			visão mais completa e acertada da realidade.
		</p>
		<p>
			Todavia existem aqueles que são intelectualmente desonestos e são muito hábeis em manipular
			com as palavras fazendo com que a verdade pareça mentira e vice-versa.
		</p>
		<p>
			Outro erro muito comum é considerar apenas um ponto de vista da história e censurar os demais.
		</p>
		<p>
			A dialética utilizada como instrumento de manipulação, consistem em um hábil jogo de palavras
			onde deliberadamente escolhe-se quais fatos considerar ou desconsiderar a fim de induzir os
			ouvintes a um determinado modo de pensar.
		</p>
		<p>
			Os fatos são suprimidos, exagerados, distorcidos, ridicularizados para que apresentem uma
			"realidade" diferente daquela que de fato ocorreu.
		</p>
		<BookTextTitle>Dialética erística</BookTextTitle>
		<BookQuote author="Hermes Machado.">
			<p>
				A vida é o teatro do engodo elaborado por uns poucos cônscios para encarcerar a grande massa
				de cegos que os seguem sem contestação.
			</p>
		</BookQuote>
		<p>
			Dialética Erística, é um importante, porém inconcluso, acréscimo do sistema filosófico de
			Arthur Schopenhauer, publicado póstumo por Julius Frauenstädt. Nele, Schopenhauer analisa os
			principais esquemas argumentativos enganosos que os maus filósofos utilizam, com razoável
			sucesso, para persuadir o público de que 2 + 2 = 5, principalmente, nos Tópicos de
			Aristóteles.
		</p>
		<p>Seguem alguns dos estratagemas de manipulação:</p>
		<BookTextTitle>Ampliação indevida ou expansão</BookTextTitle>
		<p>
			Levar a afirmação do adversário para além de seu limite natural, interpretá-la da maneira mais
			genérica possível, tomá-la no sentido mais amplo possível e exagerá-la; inversamente,
			concentrar a própria afirmação no sentido mais limitado, no limite mais restrito possível;
			pois, quanto mais genérica se torna uma afirmação, a mais ataques ela fica exposta.
		</p>
		<p>Exemplo:</p>
		<p>Interlocutor: sou a favor da pena de morte no caso de crimes hediondos.</p>
		<p>
			Manipulador: indiretamente você está me dizendo que aprova a morte de jovens, idosos, mulheres
			grávidas e até mesmo crianças. Sabemos que a lei não funciona para os ricos então só posso
			concluir que você odeia pobres e quer eliminá-los da face da Terra. Eu sou a favor da vida.
		</p>
		<BookTextTitle>Mudança de modo</BookTextTitle>
		<p>
			A afirmação que foi apresentada em modo relativo, é tomada como se tivesse sido apresentada em
			modo absoluto, universalmente, ou pelo menos é compreendida em um sentido totalmente
			diferente, e assim refutada com base neste segundo contexto. Isto faz com que pareça que o
			adversário, na realidade, fala de uma coisa distinta daquela que se havia colocado. Quando nos
			deixamos levar por este "estratagema", cometemos, então, uma "ignoratio elenchi" (ignorância
			do contra-argumento).
		</p>
		<p>Interlocutor: as vezes é necessário corrigir nossos filhos com algumas palmadas.</p>
		<p>
			Manipulador: você prega que devemos utilizar a violência para resolver os problemas sociais.
		</p>
		<BookTextTitle>Uso intencional de premissas falsas</BookTextTitle>
		<p>
			Um bom exemplo seria inventar ou exagerar estatísticas que apoiam o argumento e quando o
			argumento for contestado, é dito que não se pode provar a inveracidade do mesmo.
		</p>
		<p>Também é muito comum o uso de argumentos que provém de fontes totalmente duvidosas.</p>
		<p>Exemplo (caso real):</p>
		<p>
			Certa vez foi feita uma pesquisa de voto para ver se o presidente A ou B ganharia, o
			presidente A recebeu 90% das intenções de voto.
		</p>
		<p>
			O presidente A já tinha sido presidente no passado, a pesquisa foi feita por um órgão
			patrocinado por ele e dentro de um acampamento também financiado por ele.
		</p>
		<p>
			A mídia omitiu estes detalhes mas a discrepância foi tão aquém da realidade que as pessoas
			passaram a acusar este órgão de fazer pesquisas fraudulentas e ele teve que se explicar
			publicamente.
		</p>
		<p>
			Eles alegaram que não tiveram a intenção de serem parciais e apenas esqueceram de mencionar o
			contexto no qual a pesquisa foi feita.
		</p>
		<BookTextTitle>Encolerizar o adversário</BookTextTitle>
		<p>
			Provoca-se a cólera do adversário, para que, em sua fúria, ele não seja capaz de raciocinar
			corretamente e perceber sua própria vantagem.
		</p>
		<p>
			Quando o adversário fica irritado com algum argumento, insiste-se nesse ponto, porque
			provavelmente ali há uma inconsistência.
		</p>
		<p>Se o adversário se descontrolar, ele pode perder a razão sendo grosseiro.</p>
		<BookTextTitle>Perguntas em ordem alterada</BookTextTitle>
		<p>
			Fazer as perguntas numa ordem distinta da exigida pela conclusão que dela pretendemos, com
			mudanças de todo gênero; assim, o adversário não conseguirá saber aonde queremos chegar e não
			poderá prevenir-se.
		</p>
		<BookTextTitle>Pista falsa</BookTextTitle>
		<p>
			Se percebemos que o adversário, intencionalmente, responde pela negativa às perguntas cuja
			resposta afirmativa poderia confirmar nossas proposições, então pergunta-se o contrário da
			proposição que queremos usar, como se quiséssemos que fosse aprovada, ou então, pelo menos,
			por as duas à escolha, de modo que não se perceba qual delas queremos afirmar.
		</p>
		<p>
			Se ele está negando ou afirmando sistematicamente todas as proposições do adversário, usa-se
			uma lógica contrária a fim de fazer com que ele negue automaticamente algo que ele gostaria de
			responder como algo verdadeiro.
		</p>
		<BookTextTitle>Salto indutivo</BookTextTitle>
		<p>
			Se o adversário já aceitar casos particulares, não perguntar-lhe se admite também a verdade
			geral derivada dos casos particulares; introduz-se como se estivesse estabelecida e aceita.
		</p>
		<p>Exemplo:</p>
		<p>
			Manipulador: Adolf Hitler afirma em seu livro que alguns banqueiros judeus sabotaram a
			economia da Alemanha e com isso jogaram boa parte da população na miséria. Você concorda com
			isso?
		</p>
		<p>Interlocutor: Sim!</p>
		<p>Manipulador: então você é nazista!</p>
		<BookTextTitle>Manipulação semântica</BookTextTitle>
		<p>
			Associar a um termo um conjunto de significados diferentes do original. Com isso, o termo já
			conterá, em si, a conclusão a que se quer chegar.
		</p>
		<p>
			Podemos perceber este acontecimento na sociedade onde certas palavras são automaticamente
			ofensivas, mesmo quando ela possui várias interpretações.
		</p>
		<BookTextTitle>Uso intencional da mutatio controversiae</BookTextTitle>
		<p>
			Estratégia que consiste em “interromper o debate a tempo” quando se está ameaçado de ser
			abatido, sair do debate “ou desviá-lo e levá-lo para outra questão”.
		</p>
		<BookTextTitle>Fuga do específico para o universal</BookTextTitle>
		<p>
			Por exemplo, se temos de dizer por que uma determinada hipótese não é crível, falaremos da
			incerteza geral do saber humano, ilustrando-a com toda sorte de exemplos.
		</p>
		<BookTextTitle>Argumentum ad verecundiam</BookTextTitle>
		<p>
			Citar autoridades no assunto para refutar uma tese. Este estratagema funciona tanto melhor
			quanto menores forem os conhecimentos do adversário a respeito do que disse a autoridade
			invocada e quanto maior for a veneração dele diante de tal autoridade.
		</p>
		<BookTextTitle>Incompetência irônica</BookTextTitle>
		<p>
			Fingir que não entendeu o que o adversário disse e declarar isso ironicamente. Nas
			circunstâncias certas, isso faz o adversário parecer um idiota que não sabe organizar o
			raciocínio ou que está simplesmente declarando algo patentemente falso.
		</p>
		<BookTextTitle>Rótulo odioso</BookTextTitle>
		<p>
			Estratégia que visa reduzir uma afirmação do adversário "a uma categoria geralmente
			detestada".
		</p>
		<BookTextTitle>Tomar a prova pela tese</BookTextTitle>
		<p>
			Quando o adversário usa uma prova ruim para defender uma ideia válida, podemos nos aproveitar
			disso e provar que a ideia é inválida, a julgar pela refutação da tese apresentada.
		</p>
		<BookTextTitle>Ofensas pessoais</BookTextTitle>
		<p>
			Atacar o adversário pessoalmente, com grosseria e agressividade, quando o debate se mostra de
			todo perdido.
		</p>
		<BookTextTitle>Argumentum ad hominem ou argumentum ad personam</BookTextTitle>
		<p>
			Esta estratégia de manipulação consiste em depreciar o argumentador ao invés de refutar o
			argumento, a fim de que ele seja desacreditado.
		</p>
		<p>Exemplo (real):</p>
		<p>
			Um certo hacker invadiu alguns sistemas do governo e publicou na internet diversos documentos
			confidenciais.
		</p>
		<p>
			No dia seguinte, duas mulheres o acusaram de estupro. Após isso, a mídia só se referia a ele
			como estuprador.
		</p>
		<p>
			O fato de ele ser ou não estuprador, não muda a veracidade ou inveracidade da mensagem que ele
			passou, mas quando alguém possui um rótulo odioso ou irresponsável (como alcoolatra, drogado),
			ele passa a ser ignorado pela maioria que simplesmente não quer mais saber se ele está certo
			ou não.
		</p>
		<BookTextTitle>Argumentum ad ignorantiam</BookTextTitle>
		<p>
			A expressão latina argumentum ad ignorantiam (em português: argumento da ignorância, também
			referida como apelo à ignorância) designa uma falácia lógica que tenta provar que algo é falso
			ou verdadeiro a partir de uma ignorância anterior sobre o assunto.
		</p>
		<p>
			É um tipo de falso dilema, já que assume que todas as premissas são verdadeiras ou que todas
			as premissas são falsas.
		</p>
		<p>Estrutura lógica</p>
		<ul>
			<li aria-level="1">Não conheço <i>A</i> ou sobre <i>A</i>;</li>
			<li aria-level="1"><i>A</i> não existe ou não serve.</li>
		</ul>
		<p>Exemplos:</p>
		<ul>
			<li aria-level="1">
				"Nunca encontraram vida fora da Terra, logo não existe vida fora da Terra."
			</li>
			<li aria-level="1">"Impossível que haja vida apenas na Terra nesse imenso universo."</li>
		</ul>
		<BookTextTitle>Horror cósmico</BookTextTitle>
		<p>
			Considerando o modelo de universo mais aceito, praticamente tudo o que se compara a ele se
			torna “irrelevante”.
		</p>
		<p>
			Neste caso, o manipulador tentará uma “falsa refutação” comparando seu argumento com o
			Universo.
		</p>
		<p>
			Exemplo: Com tantas galáxias, universos paralelos e corpos celestes absurdamente grandes, você
			acha mesmo que Deus se importa com os humanos?
		</p>
		<p>
			Esta é uma falsa refutação onde assume-se a premissa de que a importância das coisas se resume
			ao tamanho das mesmas. Isso é obviamente falso, em alguns casos pode até ser, mas não é
			correto tomar isso como verdade absoluta sem analisar o contexto.
		</p>
		<BookTextTitle>Dissonância cognitiva</BookTextTitle>
		<p>
			O conceito de dissonância cognitiva remete à necessidade, do indivíduo, de procurar coerência
			entre suas cognições (conhecimento, opiniões ou crenças). A dissonância ocorre quando existe
			uma incoerência entre as atitudes ou comportamentos que acredita serem corretos e o que
			realmente é praticado.
		</p>
		<p>
			Nos dias de hoje a dissonância cognitiva é largamente utilizada a fim de enganar e causar
			confusão nas massas.
		</p>
		<p>
			O homem biológico não é homem, a mulher biológica não é mulher, o feto não é feto,
			basicamente: você não está vendo o que você está vendo. A interpretação subjetiva do que você
			está vendo deve-se sobrepor à realidade objetiva que está sendo observada.
		</p>
		<p>
			Esta estratégia de manipulação consiste em fazer um jogo de afirmações coerentes seguidas de
			afirmações incoerentes, defender um certo ponto de vista e em seguida defender o oposto do que
			foi dito.
		</p>
		<p>
			Quando isto é feito com habilidade, de modo que não pareça que o interlocutor esteja louco, o
			resultado é a confusão do ouvinte que tende a pensar que o interlocutor é inteligente demais e
			está fazendo afirmações complexas.
		</p>
		<p>
			Em meio à confusão, o ouvinte tende a permitir que o interlocutor decida por ele o que está
			correto (esse é objetivo).
		</p>
		<BookTextTitle>Sabedoria x histeria</BookTextTitle>
		<p>
			A histeria é o desligamento entre percepção e linguagem onde a narrativa (falsa) se torna mais
			importante que os fatos "palpáveis".
		</p>
		<p>Esta é outra estratégia extremamente utilizada pela mídia e pelos governantes.</p>
		<p>
			O histérico basicamente sobrepõe uma determinada narrativa à realidade. Geralmente é uma visão
			parcial, tendenciosa que dará a ele a razão nas discussões. Todavia, ele só terá tal razão se
			a realidade for encarada tal como ele encara: enfatiza-se os elementos que corroboram seu
			ponto de vista e ignora-se por completo os elementos que o refutam.
		</p>
		<p>Geralmente consegue-se sobrepor a realidade por meio de muita repetição.</p>
		<p>
			Em lugares onde os governos são ditatoriais e extremamente opressores, é muito comum encontrar
			uma legião de histéricos.
		</p>
		<p>
			A realidade da vida nestes lugares é tão horrível que os indivíduos criam uma realidade
			alternativa em suas mentes e passam a afirmá-la como real, negando a realidade objetiva que de
			fato existe.
		</p>
		<p>
			Boa parte dos movimentos reacionários fazem o mesmo, para que tenham razão nas pautas que
			defendem, não é incomum que peguem um caso isolado, que possua raríssima ocorrência e o
			apresentem como algo que acontece o tempo todo. Ou então, apontam a corrupção do movimento
			contrário ao deles e se cegam para a corrupção que está acontecendo dentro do seu movimento.
		</p>
		<p>
			Este tipo de comportamento, resume-se a um cinismo extremo, porém, é muito comum que a negação
			da realidade se dê em nível subconsciente e o resultado são histéricos que crêem
			religiosamente nas suas realidades forjadas, ao ponto de se cegarem completamente aos fatos,
			de modo que simplesmente não aceitam aquilo que é incontestável se isso vai contra aquilo que
			o histérico deseja acreditar.
		</p>
		<BookTextTitle>Politicamente correto</BookTextTitle>
		<p>Esta é uma das mais danosas estratégias de manipulação pois ela atua no âmbito social.</p>
		<p>
			Em nome da “educação”, da “não grosseria” e "gentileza", existe um consenso mútuo onde é
			permitido mentir e negar a realidade.
		</p>
		<p>
			Esta manipulação atua em níveis muito sutis de realidade, ao ponto de alterar
			consideravelmente o vocabulário e suavizar ou exagerar determinados assuntos.
		</p>
		<p>
			Um exemplo disso seriam pessoas pró aborto que chamam o feto de “bolo fetal”. Colocado nestes
			termos, o feto deixa de ser uma vida e passa a ser apenas um amontoado de células que ainda
			não se transformou em um ser vivo.
		</p>
		<p>
			Existem outras pessoas que aprovam o aborto sob o argumento de que o feto não possui
			identidade civil, ou seja, ele não foi cadastrado no sistema e portanto não pode ser
			considerado uma pessoa.
		</p>
		<p>
			O cinismo, a hipocrisia, o exagero e a condescendência são largamente utilizados neste caso.
		</p>
		<p>
			Existe um outro poderoso elemento que é a rejeição social que os não adeptos do politicamente
			correto sofrem. Eles passam a ser muito mal vistos pois não aderiram à narrativa. Esta é uma
			poderosa forma de manipulação onde alguns indivíduos aceitam defender questões que vão contra
			os seus princípios a fim de serem aceitos em grupos sociais.
		</p>
		<BookTextTitle>Histeria, psicose e esquizofrenia induzidas</BookTextTitle>
		<p>
			Outra tática muito utilizada pelos governos consiste em deixar toda a população em um perpétuo
			estado de medo. A mídia é a responsável direta por consumar esta agenda.
		</p>
		<p>É sabido que o medo crônico gera uma série de problemas mentais.</p>
		<p>Qualquer um que esteja com medo, acolherá de bom grado qualquer salvador que aparecer.</p>
		<BookTextTitle>Mundanismo, materialismo excessivo</BookTextTitle>
		<p>
			As propagandas disparam o tempo todo a informação subliminar de que você só é uma pessoa
			digna, se for capaz de comprar certas coisas.
		</p>
		<p>
			Considerando a pirâmide de Maslow, podemos ver que toda a publicidade é voltada para os 3
			primeiros degraus somente. Não há transcendência nem engrandecimento pessoal.
		</p>
		<p>
			As pessoas são condicionadas a associarem sua auto estima ao seu poder de compra; como boa
			parte da sociedade pensa desta forma, a aceitação ou rejeição de uma pessoa pode ser
			totalmente definido com base no poder aquisitivo; uma vez que a rejeição acontece, a pessoa
			tende a gostar de si mesma somente se puder alcançar certos patamares do materialismo.
		</p>
		<p>
			A auto estima de uma pessoa deveria ser definida por aquilo que ela é, faz e o que deseja ser,
			principalmente em países onde os tributos são abusivos e a desigualdade social é muito grande.
		</p>
		<BookTextTitle>Manipulação do ego</BookTextTitle>
		<p>
			Todos os seres humanos possui egocentrismo, em maior ou menor grau. Todos possuem os mesmos
			sentimentos em graus variados. Isto torna os seres humanos previsíveis (até certo ponto,
			obviamente). Você não consegue prever facilmente a reação de alguém acerca de algum
			acontecimento, mas consegue prever com maior facilidade os possíveis sentimentos que a pessoa
			terá ao se deparar com um acontecimento qualquer.
		</p>
		<p>
			A bajulação, quando aplicada de modo a não parecer falsidade, é extremamente eficaz na
			manipulação de alguém.
		</p>
		<p>Quem é que não gosta de ser elogiado (a)?</p>
		<p>
			Sun Tzu em "a arte da guerra" enfatiza o quanto a manipulação do ego do adversário pode ser
			eficiente para trazer vitória.
		</p>
		<p>
			Certa vez, uma das cidades sob posse de Adolf Hitler foi recuperada pelos aliados. Hitler
			ficou tão enfurecido que enviou todas as tropas das cidades ao redor para retomá-la.
		</p>
		<p>
			Em outro momento, os aliados recuperaram outra cidade e Hitler lidou com a situação da mesma
			forma.
		</p>
		<p>
			O episódio se repetiu novamente, só que desta vez os aliados anteciparam a situação e assim
			que Hitler desguarneceu as cidades ao redor, elas foram tomadas pelos aliados.
		</p>
		<p>
			Hitler não admitia derrota em hipótese alguma, isso fez com que ele se tornasse previsível.
		</p>
		<p>
			Episódios como os mencionados se repetiram até o fim da guerra e a conduta de Hitler foi
			crucial para a vitória dos alidados.
		</p>
		<p>
			Nestes episódios era muito comum que Hitler ignorasse completamente as instruções dos generais
			que estavam em campo de batalha.
		</p>
		<p>
			Hitler possui a fama de ser inteligente e possuir grande oratória, mas ao ser atacado, se
			descontrolava emocionalmente e isso lhe trouxe a derrota.
		</p>
		<BookTextTitle>A estratégia dos 101%</BookTextTitle>
		<p>
			Esta estratégia é aplicada em pessoas que têm muito pouco ou quase nada em comum com o
			manipulador.
		</p>
		<p>
			Quando ele percebe isso, tentará descobrir o 1% que possui em comum com a pessoa e dará 100%
			de importância para esta questão.
		</p>
		<p>
			É uma tentativa de se forçar a criação simpatia, visto que o manipulador exagerará a
			importância daquele ponto em questão.
		</p>
		<BookTextTitle>A estratégia do inimigo em comum</BookTextTitle>
		<p>
			Esta estratégia consiste em criar um inimigo em comum a fim de que a outra pessoa se torne sua
			aliada.
		</p>
		<p>
			Vemos isso largamente hoje em dia, várias pessoas se unem para lutar contra os fascistas
			nazistas comunistas; todos eles imaginários ou sendo combatidos em um contexto onde eles
			simplesmente não existem.
		</p>
		<p>
			A ideia de um inimigo em comum tende a unir pessoas quase que automaticamente. Essa ideia é
			tão poderosa que pode unir inimigos, ainda que temporariamente a fim de derrotar o inimigo em
			comum.
		</p>
		<BookTextTitle>A estratégia do espantalho</BookTextTitle>
		<p>
			Consiste em uma tentativa de manipulação onde o manipulador alega que certa pessoa
			(espantalho) ou certo grupo de pessoas são os responsáveis por todas as coisa ruins que já
			aconteceram na sua vida.
		</p>
		<p>Esta manipulação é extremamente utilizada por políticos e líderes religiosos.</p>
		<p>É uma estratégia muito sedutora que consegue adeptos facilmente.</p>
		<p>
			O manipulador convence a pessoa de que a vida dela seria perfeita se o "espantalho" não
			existisse.
		</p>
		<p>
			Uma vez aceita essa ideia, a pessoa perde seu senso de responsabilidade por seus próprios
			atos, ela para de se culpar pelos erros do passado e ganha aliados contra um inimigo em comum.
		</p>
		<p>
			Isso cria simpatia mútua, sensação de acolhimento e de autruísmo, visto que todos os adeptos
			lutarão contra o inimigo.
