			as vibrações críticas na estrutura do avião (...). Desde o ponto de vista da engenharia, isto
			significa que as forças e as debilidades da estrutura do avião em termos da energia vibratória
			podem ser descobertas e manipuladas.
		</p>
		<BookTextTitle>Aplicação à economia</BookTextTitle>
		<p>
			Para utilizar este método de "teste de choque" aeronáutico na engenharia econômica, os preços
			dos produtos estão submetidos a um choque, e a reação do público é medida.
		</p>
		<p>
			O eco resultante do choque econômico é interpretado teoricamente pelos computadores e a
			estrutura psyco econômica é assim descoberto.
		</p>
		<p>
			É por este procedimento que é descoberto o que define o lar familiar e faz possível a sua
			avaliação (veja-se avanços de econometria).
		</p>
		<p>
			Desde então, a resposta do lar e família ao manejo dos choques futuros pode ser prevista e
			manipulada, e a sociedade converte-se então num animal bem regulado com as suas renas sob o
			controle de um sofisticado sistema de contabilidade de energia social regulado por computador.
		</p>
		<p>
			Finalmente, cada elemento individual da estrutura esta sob o controle de um computador através
			do conhecimento das preferências pessoais, um tal conhecimento é estendido pela associação
			informática de códigos de barra com a identificação exata do consumidor identificado (através
			do cartão de crédito, e mais tarde com o tatuagem permanente sobre o corpo de um número
			invisível sob a luz ambiente ordinária).
		</p>
		<p>
			Graças à prova de choque (teste de choque), foi descoberta uma relação direta entre a
			disponibilidade do fluxo de dinheiro numa economia e a resposta de uma massa de gente em
			função a essa disponibilidade.<br />Por exemplo, estabeleceu-se que existe uma relação
			quantitativa mensurável entre o preço da gasolina e a probabilidade de que pessoa sofra de dor
			de cabeça, ressinta vontades de ver uma película violenta, fumar um cigarro, ou ir a um bar
			para tomar um par de cervejas.
		</p>
		<BookTextTitle>Indução econômica</BookTextTitle>
		<p>
			Um indutor elétrico tem uma corrente elétrica como primeiro fenômeno, e um campo magnético
			como segundo fenômeno (inércia). Corresponde a isto, um indutor econômico a um fluxo de valor
			econômico como primeiro fenômeno, e um campo de população como segundo fenômeno de inércia
		</p>
		<p>
			Quando o fluxo de valor econômico (isto é o dinheiro) diminui, o campo de população humana
			desaparece com o objetivo de permitir ao valor econômico de seguir circulando (caso extremo:
			guerra).
		</p>
		<BookTextTitle>Amplificadores econômicos</BookTextTitle>
		<p>A forma mais simples de amplificador econômico é um instrumento chamado publicidade.</p>
		<p>
			Se uma publicidade televisiva se dirige a uma pessoa como se ela tivesse 12 anos de idade,
			então, em razão da sugestibilidade, ela terá, com uma verdadeira probabilidade uma resposta ou
			uma reação tão desprovida de sentido crítico que aquelas pessoas com uma idade de 12 anos.
		</p>
		<BookTextTitle>Consentimento, a primeira vitória</BookTextTitle>
		<p>
			Um sistema de arma silenciosa opera a partir de dados (informação) obtida de um público dócil
			por meios legais. Tanta informação se acha disponível para os programadores de sistemas de
			armas silenciosas através do Internal Revenue Service. (Ver Estudos na Estrutura da econômica
			americana para uma lista de fontes I.R.S.).
		</p>
		<p>
			A informação consiste na entrega obrigatória de dados bem organizados conteúdos nos
			formulários de impostos federais ou nacionais, coletados, sistematizados, e apresentados pelos
			mesmos pagadores de impostos e os empregados.
		</p>
		<p>
			Ademais, o número de tais formulários submetidos à I.R.S. é um indicador útil do consentimento
			do público, um fator importante na tomada de decisão estratégica. Outras fontes de dados são
			expostos na curta Lista de inputs.
		</p>
		<p>
			Os coeficientes de consentimento são um feedback numérico indicando o estatuto ou grau da
			vitória. Base psicológica: Quando o governo é capaz de coletar ou arrecadar os impostos e de
			dimensionar a propriedade privada sem justa compensação, é uma indicação que o público esta
			maduro para render-se e consentir sua posta em escravatura e a seu submetimento legal. Um bom
			indicador, facilmente quantificável, de tempo mais duros no futuro é o numero de cidadãos
			públicos que pagam um imposto mesmo ante uma carência evidente de reciprocidade ou de serviço
			honesto por parte do governo.
		</p>
