Choque de realidade e recalibração do sistema emocional

Os covardes herdarão o inferno!
O politicamente correto irá te matar!
Você está sendo largamente enganado!

É ele que te faz pedir desculpa por ser homem, branco, heterossexual.
É ele que te faz pedir desculpa por ser conservador, patriota, humano.
Eles te fazem sentir vergonha da sua decência, da sua dignidade, do seu caráter.
Eles fazem você odiar a si mesmo(a) e a todos ao seu redor.

Eles destróem tudo e você aplaude, como gado indo para o abate.
Eles te adoecem, te empobrecem e você os idolatra como salvadores.
Eles te fazem odiar a vida e você os defendem como se fossem deuses.
Eles censuram a informação, te manipulam, te transformam em um fantoche que não sabe pensar.

São eles que criam as guerras, as fomes, as pestes e todas as mazelas da humanidade.
Eles praticam o culto a morte aos olhos de todos e você acha lindo.
Você vota neles, mesmo sabendo que são criminosos que nem se preocupam em disfarçar.
Você os defende, pois eles lutam pela sua agenda.

Não, idiota! Você é quem luta pela agenda deles.
Você tem sido doutrinado pela mídia, filmes, militâncias pagas e por professores que não sabem nada.
Eles te ensinaram a ser falso(a) e cínico(a) como eles o são.
Seus pensamentos não são seus, foram incutidos na sua mente, como um vírus.

Você chama a moralidade de patriacardo, família de opressora, a vida de doença.
Você chama a destruição de libertação, a morte de vida, a mentira de verdade.
Você chama a escravidão de liberdade! Chama roubo de justiça!
Você diz ser livre mas não pode nem pensar. Seu grupo lhe deserdaria imediatamente se você ousasse questionar uma pauta sequer.

Você se diz defensor da liberdade mas é um(a) ditador(a).
O contraditório jamais é aceito, é demonizado e caçado como um monstro digno de morte.
Você promove cancelamentos, censura, perseguição.
A sua arte, se resume a enfiar o dedo no anus de outros em público, e acha que isso salvará o mundo.

Você se diz livre de preconceitos, mas jamais aceita o diferente.
Não obstante, quer obrigar a todos a pensarem como você pensa.
E sua arrogância te faz pensar, que sua agenda esdruxula é a salvação do mundo.
Mesmo podendo assistir, em tempo real, esta mesma agenda destruir a tudo o que toca.

Você já não pensa mais. Você é um(a) robô programado(a) para repetir o que foi enxertado em sua mente.
Você é incapaz de decidir sua própria identidade e quer me obrigar a acreditar que a sua agenda salvará a minha vida.
Você concorda que outros sejam roubados, pois são ricos e o fruto do roubo será repassado para você (em tese).
Você aplaude a perseguição e a punição de pessoas honestas, cujo o único crime é pensar diferente de você.

Como papagaio, você repete tudo o que eles dizem, sem questionar.
Você sacrificou seu senso de justiça em nome da aceitação social.
Você já teve sua mente sequestrada. Até onde você irá com esta mitomania?
Você realmente acha que abster-se da justiça e apoiar criminosos vai acabar bem?

Eles te ensinam a ser covarde, irresponsável, hedonista e alienado.
Moldam o seu sistema emocional para ser mais frágil que o de uma criança.
Elem te fazem odiar os amigos e idolatrar os inimigos.
Eles te convencem que você é uma eterna vítima, de tudo e de todos.

Você sabe que tudo o que eu disse é verdade, mas ainda sim me chamará de terrorista.
Diz que odeia a injustiça mas ama uma das doutrinas mais genocidas da face da Terra.
Seu senso de proporção simplesmente não existe e sua balança tem pesos diferentes.
A hipocrisia é a base do seu discurso, sempre!

Você chama velhinhas de terroristas e genocidas de heróis.
Você é idólatra de assassinos e de gente da pior espécie!
Você é um(a) ditador(a) que jamais aceita o contraditório!
Você quer obrigar todos a viverem nesse seu mundo de mentiras.

O preço da covardia é se sujeitar deliberadamente aos horrores que estão por vir.
O preço da irresponsabilidade ao se informar, será ser pego de surpresa e não saber o que fazer.
O preço da adesão ao politicamente correto é ser massa de manobra e perder sua identidade como ser humano!
Você presume ser um servo da Luz, mas você é um sacerdote da irmandade da serpente!

Acorde! Enquanto te lobotomizam para acreditar que a grama é rosa, estão executando um plano genial para matar você!

