A serpente alada é o símbolo da sabedoria, do conhecimento, da ciência, da medicina, da cura, da imortalidade, da vida eterna.
A serpente rastejante é o símbolo da ignorância, da estupidez, da doença, da morte, da destruição, da escravidão, da morte eterna.

Isto é parafraseado pelo apóstolo Paulo quando diz: "a letra mata, o espírito vivifica".

A doutrina Vivifica pelo Espírito da Verdade fará com que seu eu seja capaz de ler algo e extrair amor e benevolência, podendo resultar em sublimes manifestações do Logos.

A letra sem espírito, te permitirá facilmente utilizar, por exemplo, a Bíblia para criar uma religião que defenda a extinção de certos homens com base no DNA (eugenia).

