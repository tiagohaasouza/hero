<h1>Armagedom</h1>

<p>Toda a conspiração que você ouviu até agora, é para este momento: a batalha do Armagedom, a batalha final entre o bem e o mal.</p>
<p>Os demônios já foram julgados e condenados á morte. Eles convenceram os Illuminati de que para eles, também não há salvação.</p>
<p>Eles estão desesperados. Sabem que morrerão espiritualmente.</p>
<p>Somente Deus sabe explicar o mistério da iniquidade, mas podemos deduzir que o desespero gera insanidade. Como diz o ditado:</p>

<quote>Situações desesperadoras requerem medidas desesperadas.</quote>

<p>A sinagoga de Satanás não tem esperança de vida eterna. Eles não creem na possibilidade de salvação das suas almas.</p>

<p>Eles sabem que está próximo o tempo da execução do veredito celeste.</p>

<p>Eles sabem que a volta do cordeiro está próxima e eles recorreram à única coisa que podem fazer.</p>

<p>O reino das trevas usou de toda a malícia e engano para recrutar o máximo de humanos possíveis, a fim de que estes lutem em favor das trevas na batalha do Armagedom.</p>

<p>Esta é uma vã tentativa de escapar do juízo Divino.</p>

<p>O desespero e a loucura oriundos da certeza de morte espiritual fez com que os demônios e seus prepostos ousassem afrontar a Cristo e seus anjos em sua segunda vinda.</p>

<p>Eles alegam que preferem morrer com sua honra ao invés de se submeterem a um Deus tirânico que não aceita estar errado e muito menos aceita que eles sejam mais inteligêntes que o próprio Deus.</p>

<p>Todavia isso é vaidade e inveja. Houve um tempo onde os demônios acreditavam nestas coisas com sinceridade. Mas o Pai de Todos já provou exaustivamente que eles estavam errados. Deus mostrou à todo o universo que as conclusões demoníacas se deram porque estes não conseguiram compreender a profundidade dos pensamentos de Deus.</p>

<p>Sabemos que nenhum deles acredita mais nos motivos primários da rebelião. No momento, todos eles assumiram a postura sínica de não admitir o erro, tal como humanos fazem.</p>

<p>Eles sabem que estão errados mas preferem morrer à admitir isso. Eles escolheram morrer ao invés de admitir e em breve morrerão.</p>

<p>Houve um tempo que no coração destes seres, existia um genuíno desejo de justiça, ainda que as motivações estivessem equivocadas, hoje só existe ódio em seus corações.</p>

<p>Eles não amam sequer a si mesmos, mas ainda sim temem a inexistência, todavia, preferem a inexistência ao invés de se submeterem à Luz.</p>

<p>Como eu disse, somente Deus pode explicar o mistério da iniquidade</p>