Case-se

[Casamento entre o cristo interno e o Cristo externo]

No altar da alma, o amor se desvela,
O cristo interno, chama que se acende,
Imanente luz que em cada véu se revela,
Na dança cósmica, eternamente estende.

Do íntimo ser ao divino além,
Unem-se laços, transcendente e humano,
Casamento sagrado, como um poema,
Onde o etéreo e o terreno se entrelaçam, ufano.

O cristo interno, centelha de divindade,
Desperta anseios, sonhos profundos,
Tece esperanças com terna claridade,
Ecos do eterno em momentos fecundos.

Cristo externo, o cósmico e espiritual,
Além dos véus, o universo se expande,
No firmamento estrelas traçam seu sinal,
Na dança do tempo, a existência se bandeia.

No enlace desses cristas, o transcendente e imanente,
A união é um hino, é melodia em ascensão,
É o amor em forma, em cada ser latente,
O divino entrelaçado na jornada em evolução.

Assim, no casamento do cristo interno e externo,
A alma dança a sinfonia do etéreo e finito,
É a busca constante, um amor fraterno,
Na união dos opostos, no divino infinito.

No altar da alma, o amor se revela,
Cristo interno e externo, união que encanta,
Imanente luz e transcendência bela,
Dançam juntos na jornada que espanta.

Do íntimo ser ao divino mais além,
Laços se entrelaçam, humano e cósmico,
Casamento sagrado, como um poema além,
Eterno e finito, em união magnífica.

Cristo interno, centelha de divindade,
Desperta sonhos profundos, anseios no peito,
Tece esperanças com doce claridade,
Ecos do eterno, momentos perfeitos.

Cristo externo, cósmica espiritualidade,
Amplia horizontes, universo em expansão,
No firmamento, estrelas dançam com vitalidade,
Tempo e existência, em constante refrão.

Na união desses cristas, imanência e transcendência,
A melodia do amor ressoa em compasso,
A jornada é um hino, em doce existência,
Divino entrelaçado, em amor abraço.