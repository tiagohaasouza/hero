Conceitos sobre espírito:

* É o folego da vida indestrutível, que existe desde sempre e durará para sempre.
* É o folego da vida porém pode morrer, neste caso, a morte não se daria por 'morte natural', ou seja, o espírito é naturalmente imortal mas seu comportamento pode levar à morte. Neste caso, o que levaria à morte seria a desobediência de certos decretos Divinos muito específicos, neste caso seriam pecados gravíssimos que demandariam um certo grau de consciencia para serem praticados pois o nível de maldade teria que ser grande.
* Só existe um único Espírito, o de Deus. Os seres vivos, só o são porque o Espírito de Deus anima e sustenta as suas almas. Neste entendimento, é comum dizerem que o mundo espiritual é elétrico e o Espírito seria algo como a bateria de um aparelho, se o Espírito adentra à alma, ela liga, caso contrário, ela desliga.

Algumas doutrinas alegam que o ego deve ser exterminado, outras dizem que o mesmo deverá ser reafirmado com toda veeência possível.

Doutrinas que defendem a reafirmação do ego, dizem que o mesmo é uma parte essencial da individualidade e que a mesma deve ser preservada. Ao eliminar o ego, o indivíduo se 'dissolveria' no absoluto, causando uma espécie de suicídio espiritual do eu. Essa linha de afirmação do ego possui raízes gnósticas e dizem que o objetivo da vida é justamente se desvencilhar totalmente das egrégoras de pensamento coletivas pois quanto mais imerso um indivíduo esteja nelas, menos identidade pessoal ele possui e mais sujeito ás influencias dos arquétipos de pensamento de massa ele estará.

Aqui também há uma imensa confusão. No geral, estas doutrinas não fazem a distinção entre o arquétipo mundo e o arquétipo Deus. Deste modo, logo chegam à conclusão que as duas coisas são a mesma e concluem que Deus é mal.



A questão pertinente ao ego então, não é sobre a individualidade em si, mas sim sobre a vontade do ego em questão. Este ego renunciará à sua própria vontade e cederá à vontade de Deus ou tentará impor sua vontade à todo custo?