Este é um dos assuntos basilares e existe muita confusão.

Primeiramente vamos listas as definições de ego que serão pertinentes ao nosso diálogo:

* O ego é a personalidade do indivíduo, que é única em todo o universo.
* O ego é a alma (que de certo modo pode ser considerado como a personalidade também).
* O ego é o espírito, isto é, o deus interno, o deus imanente em cada um.
* O ego é a vontade do indivíduo. Neste caso, o ego não possui ligação com a natureza do indivíduo mas sim com o comportamento.

