Percebendo isso, o maligno enche esta pessoa de vaidade, presunção e engano a fim de que ela caminhe cada vez mais em direção ao mal. O carma continua funcionando para essa pessoa, todavia, como ela é considerada um bom instrumento para o mal, os próprios demônios manipulam tanto quanto podem, o carma desta pessoa.

Eles não alteram a lei de Deus. Eles jogam com a mesma, como eles conseguem ver as linhas de tempo humanas, eles influenciam a pessoa a fim de que ela se desvie do carma. Isso faz com que ele se acumule. A pessoa não sabendo disso, se torna cada vez mais ousada em sua malignidade e a cada dia que passa, ela se torna mais e mais iníqua e mais convencida de que nunca responderá pelos seus atos, seja por se achar a pessoa mais esperta do mundo, seja por pensar que Deus não existe, seja por pensar que seu dinheiro ou influência sempre o (a) salvará.

Esta pessoa, na maioria das vezes, é completamente cega espiritualmente e por isso, é incapaz de perceber a manipulação maligna. É questão de tempo até que os demônios cometam algum erro ou Deus determine algum juízo e neste caso, a ruína é súbita e catastrófica.

