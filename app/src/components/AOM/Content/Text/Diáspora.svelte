A diáspora judaica é o termo usado para descrever a dispersão do povo judeu fora da região da Palestina, especialmente após a destruição do Segundo Templo de Jerusalém pelos romanos em 70 d.C. Muitos judeus foram mortos ou escravizados e os sobreviventes foram obrigados a fugir para outras regiões do Império Romano.

A destruição do Templo de Jerusalém foi um evento crucial na história judaica, pois o Templo era o lugar central de adoração e culto para o povo judeu. A rebelião dos judeus contra o domínio romano, liderada por Eleazar ben Simon e José ben Matéus, entre outros, foi brutalmente suprimida pelo general romano Tito e seu exército em 70 d.C.

Flávio Josefo, historiador judeu, foi um dos poucos sobreviventes da rebelião e escreveu uma história detalhada sobre os eventos. Segundo ele, Tito, filho do Imperador Vespasiano, não queria destruir o Templo, mas foi desobedecido pelos soldados romanos que, ao ver a oportunidade, acabaram destruindo o Templo.

O exército romano que destruiu o Templo era composto principalmente por soldados italianos, mas também incluía soldados de outras partes do Império, como germânicos, espanhóis e egípcios. Enquanto alguns relatos sugerem que Tito tentou evitar a destruição do Templo, outros afirmam que ele ordenou a destruição como forma de punir os rebeldes judeus.

Após a destruição do Templo, muitos judeus foram mortos ou escravizados, e os sobreviventes foram obrigados a fugir para outras regiões do Império Romano. A diáspora judaica começou e o Templo nunca mais foi reconstruído (mas está sendo reconstruído e seu término não deve demorar). A destruição do Templo foi um evento crucial na história judaica, pois o Templo era o lugar central de adoração e culto para o povo judeu. A partir dali, o judaísmo começou a se desenvolver de uma religião centrada em um lugar específico para uma religião centrada na Torah e nas tradições orais.







