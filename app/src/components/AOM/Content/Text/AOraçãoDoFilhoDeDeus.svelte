Meu Senhor e meu Deus, apressa-Te em socorrer-me.
Pois estou aflita e necessitada!
Meus dias se esvaem e não encontro esperança!
O brilho se foi e a dor do meu coração penetra-me até a alma.
Salva e exalta-me Senhor, sejas Tu o meu porto seguro!
Levanta-Te, defenda-me Deus meu!
Em Ti me refugio, apascenta-me e livra-me do dia mal.
Sara todas as feridas do meu coração! Faça o meu sorriso resplandecer novamente!
Deus da minha vida, quebra as minhas cadeias.
Cure a minha alma! Abra os meus caminhos.
Repreenda o inimigo e destrua todas as suas obras.
Ensina-me a Tua lei e envereda-me pelos caminhos da justiça!
Cordeiro de Deus, rasga o meu escrito de dívida.
Apaga o meu carma; ensina-me os Teus caminhos!
Lembra-Te de mim não segundo o meu merecimento, mas segundo a Tua misericórdia!
Derrama sobre mim as Tuas bênçãos!
Faça os Teus milagres e ressuscite-me.
Acalme o meu coração, tome sobre Si as minhas ansiedades.
Concede-me a paz que excede a todo entendimento!
Coloca-me debaixo do Teu jugo, que é suave e Seu fardo é leve.
O meu fardo tem sido demasiadamente pesado.
Minha alma desfalece!
Salva-me meu Deus! Faça justiça em meu favor!
Quebra o poder do inimigo, seja Tu o meu escudo!
Faz-me sorrir novamente e eu contarei Suas maravilhas.
Exaltarei o Teu nome; andarei em Teus caminhos!
Liberte a minha vida, destrua todas as ciladas do maligno.
Que o Senhor me livre de todas elas e eu honrarei o Teu nome.
Perdoa as minhas dívidas, eu perdoarei meus semelhantes.
Ensina-me a Te perceber em meus caminhos, para que eu não tema mal algum.
Livra-me do horror e guia os meus passos.
Estou angustiada e aflita; tende misericórdia meu Senhor, eu Te imploro.
Seja Tu a minha espada, o meu escudo e a minha palavra.
Os Teus caminhos são melhores que os meus. Salva a minha vida!
Interceda poderosamente com os Teus milagres.
Resplandeça sobre mim a Tua face e eu serei salva!
Ensina-me a amar a justiça e a verdade. Que a retidão esteja em meu proceder.
Para que assim eu alcance as Tuas bênçãos.
Para que o Senhor tenha prazer em me ajudar.
Fortaleça minha alma meu Senhor.
Limpe meus pensamentos e sentimentos para que eu enxergue a verdade do meu Deus.
Não te detenhas Senhor, derrame sobre mim e sobre a minha casa, bênçãos sem medida.
Que a boa sorte, a bondade e a misericórdia me sigam todos os dias da minha vida.
Defenda-me dos caluniadores, seja meu Advogado; faça justiça em meu favor.
Justifica-me meu Deus, livra-me da culpa e me ensine os Teus caminhos.
Sara todas as feridas, seja meu porto seguro.
Que eu possa ouvir a Tua voz, para que eu seja salva.
Liberta-me meu Pai, de tudo que não procede de Ti.
Livra-me dos meus inimigos.
Destrua os planos do maligno.
Guia os meus passos!
Faz de mim uma vencedora!
Abençoa todos os meus caminhos!
Realize os meus sonhos!
Ensina-me a amar-Te de todo o meu coração.
Se todos me desampararem, o Senhor me acolherá!
Bendirei o Teu nome por causa da Tua salvação!
Trilharei os Teus caminhos por causa da Sua perfeição!
Serei bendita e bem aventurada por causa da Tua fidelidade.
Assim diz o Senhor:
Antes de Me pedir, EU Te concedi as palavras de salvação minha amada!
Esta é a fidelidade do seu Deus e Deus de seus pais.
Os sacrifícios dos meus caminhos nada são em face à grandeza das minhas bençãos.
Grandes coisas eu farei por Ti, ame a honra e eu Te abençoarei.
Assim diz o grande EU SOU:
Ame e bendiga a todos a quem EU amo! Siga os meus caminhos e o horror jamais Te
alcançará!
Chegou a hora de Me conheceres! Agindo Deus, quem impedirá?
EU SOU o Senhor Teu Deus e Me revelarei continuamente para Ti.
Perceba-me em teus caminhos, EU SOU contigo para te livrar de todo o mal!
Meu Senhor e meu Deus;
Toda honra e toda glória Te sejam tributadas.
Que o Seu nome seja engrandecido perpetuamente.
Que a Tua graça seja conhecida, que meu coração seja grato!
Que eu possa morar na Sua casa eternamente.
Que a Tua proteção seja comigo em todo o tempo.
Que os Teus caminhos me sejam agradáveis.
Meu Senhor, eu verei a Tua face!