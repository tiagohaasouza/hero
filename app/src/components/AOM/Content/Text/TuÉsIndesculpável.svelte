







Então, Deus dirá a você:

É melhor para o universo que você não exista!

E você, diante do Senhor, despojado de toda a mentira e assim sendo obrigado a confrontar a verdade e dizer a verdade, dirá!

Tu, o Senhor Soberano, tem razão!

Então Deus te dirá:

Pois bem, que se cumpra em ti, a morte que escolheste!

Eu te peço por um momento que despoje-se do raciocínio luciferiano para que você compreenda as palavras à seguir:

Não é você que 'encontra' Deus, é Deus que escolhe se revelar à você!

Se Deus escolheu permanecer oculto à você, você não irá encontrá-lo em nenhuma filosofia, religião, ciência, ou qualquer outra coisa que você possa imaginar!

Se Ele se ocultou de ti, você não o encontrará em sua mente! Todo e qualquer raciocínio sobre Deus, te levará à convicção de que Ele não existe, pois Ele se escondeu de você!

A pior coisa que se pode suceder a um homem, é Deus escolher deixar com que este trilhe o seu caminho de perdição. Deus corrige os passos daqueles que Ele ama, de modo que os recoloca continuamente no caminho da vida!

Todavia, ao homem que tem pacto com a morte e a mentira, Deus permite que ele siga o seu caminho! Ele, em sua arrogância, mais uma vez se engrandecerá e proferirá a sua estupidez, dizendo:

Se Deus de fato existisse, Ele não permitiria que eu fizesse o que estou fazendo! Eu pratico o mal e a mentira, eu semeio a morte e sou cada vez mais próspero. As aflições que estão sobre aqueles que se dizem servos de Deus não me aflige, pelo contrário, sou honrado continuamente, tenho muito mais bens do que eu preciso, faço o que quero e não sofro repreensão alguma. Certamente sou superior em meu modo de pensar, minha prosperidade e meu sucesso são a prova de que eu estou certo!

Filho da morte, sua soberba te enganou! Tu és muito desprezado(a)! A Eternidade não te quer! A Verdade não te quer! A Vida não te quer!

Se você se curvar diante do Todo Poderoso, talvez você encontre a redenção! Mas nós já sabemos a sua escolha, não é mesmo?

Entre ser eterno servo do Amor Supremo e a morte, você escolheu a morte, pois você na condição de 'deus', não se curva à ninguém! Você não fará a vontade de ninguém! Você nasceu para que sua vontade seja feita pelos outros, os inferiores, são escravos e nada mais! Comedores inúteis!

Você é um parasita, nada mais que isso! Continue trilhando ousadamente o caminho para o inferno! Certamente você chegará lá! De Deus não se zomba e qualquer que se engrandeça sobre Ele, não permancerá de pé!

Atente-se para a profecia:

Assim diz o Senhor:

-- Serás próspero em seus caminhos, seus desígnios serão alcançados, tu consolidarás a sua maldade na Terra e não serás impedido; todavia, quando seu pé resvalar, tu cairás e não poderás mais levantar-te, pois a tua maldade te levará à morte! Ninguém irá te salvar, não haverá escape para ti e sua monstruosidade será revelada à todos! A desonra será sua companheira  e não haverá consolação que te conforte! A sua ruína será perpétua!

-- Tu que és honrado por falsos bajuladores e por temerosos do seu poder, zombarão de ti! Tu serás opróbrio e ignomínia!

-- Eu desfarei a maldade que tu semeaste na Terra e enxugarei as lágrimas dos meus filhos que Tu fizeste chorar! Restaurarei a vida deles e será como se nunca tivessem sofrido o mal que tu lhes impuseste!

-- Está firme o teu coração?

-- Prepara-te para se encontrar com o teu Deus!
