<h2>Os monges da morte</h2>

<BookQuote>

	Ficamos com o que matamos!

</BookQuote>

Caligastia, que desceu ao abismo do Subverso, trouxe de lá, a filosofia da morte.

É o fim da infância! Arthur Charles Clarke avisou!

Desde de que Caligastia subiu do abismo, algo havia de diferente em seu olhar.

Havia um cheiro de podridão, seus olhos não mais refletiam a luz do Sol.

Era como se ele tivesse perdido a alma, sua pele era pálida e seus olhos pareciam carregar a negrura infinita.

Suas palavras eram pesadas, o som de sua voz, como que de um morto.

Não havia alento de vida naquela carcaça, havia apenas aquilo que eu posso chamar de: morte em vida.

Essa vida estranha, tal como anti-vida, possuía uma magnetismo estranho, era repugnante, porém atraente.

Assombrosa e atraente, uma tentação. A sua doutrina era impressionante, todos se calavam diante daquela sabedoria.

Meu espírito gritava dentro de mim, ardentemente ele dizia: fogo estranho, fogo estranho! Este fogo, não é santo! Este fogo não é quente!

Então a minha alma entendeu! Aquele fogo era pura morte, mas sensualmente, simulava vida! Uma voluptuosa forma de vida, que é a morte!

Eu a rejeitei e então, Melquisedeque disse em meu interior:

